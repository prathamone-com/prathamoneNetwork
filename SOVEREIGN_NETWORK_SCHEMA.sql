-- EXAMPLE POLICIES (Multi-tenant)
-- (Simplified for initial draft — to be expanded in Phase 1)

CREATE POLICY "Users can only view data from their own institute"
ON public.profiles
FOR SELECT
USING (institute_id = (auth.jwt() ->> 'institute_id')::uuid);

CREATE POLICY "Students can only see their own progress"
ON public.student_progress
FOR ALL
USING (student_id = auth.uid());

CREATE POLICY "Parents can see their linked students' progress"
ON public.student_progress
FOR SELECT
USING (student_id IN (SELECT student_id FROM public.students WHERE parent_id = auth.uid()));

-- LECTURE ACCESS (Public to Institute students)
CREATE POLICY "Institute students can see lectures"
ON public.lectures
FOR SELECT
USING (institute_id = (auth.jwt() ->> 'institute_id')::uuid);
